class PromotionRepository extends AbstractRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Promotion::class);
    }

    public function add(Promotion $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Promotion $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    // RÃ©cupere toutes les marques 
    public function getQbAll(): QueryBuilder {
        $qb = parent::getQbAll();
        return $qb->select('promotion', 'count(product) AS NbProduct')
            ->leftjoin('promotion.products', 'product')
            ->groupBy('promotion')
            ->orderBy('promotion.name', 'ASC')
        ;
    }

